# Functions in Python

# Functions allow you to group code into a reusable block. 
# This prevents repetition and makes the program easier to maintain.

# Syntax and Rules
# Definition: 
# You define a function using the def keyword, followed by the name and a colon.

# Indentation: 
# Python relies on indentation (whitespace) to determine which lines of code belong to the function. 
# All logic inside the function must be indented.

# Naming Conventions:
# Function names should be descriptive and use lowercase letters with words separated by underscores (snake_case).


# The Function Call: 
# Writing the function definition does not execute the code. You must "call" the function by its name for the code to run.

# Example of a simple function that prints a greeting:
def greet():
    print("Hello, welcome to the world of Python functions!")
# Calling the function
greet()